// get name function
function getNameByLanguage(item) {
  switch (globalLanguage) {
    case "ESP":
      return item.name;
    case "ENG":
      return item.nameEnglish;
    case "FRA":
      return item.nameFrench;
    case "ITA":
      return item.nameItalian;
    default:
      return item.name;
  }
}

